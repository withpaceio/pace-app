export function convertKilometersToMiles(distanceInKilometers: number): number {
  return distanceInKilometers * 0.621371;
}

export function convertKilogramsToPounds(weightInKilograms: number): number {
  return weightInKilograms * 2.20462262185;
}

export function convertPoundsToKilograms(weightInPounds: number): number {
  return weightInPounds / 2.20462262185;
}
